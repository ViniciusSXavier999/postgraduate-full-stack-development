package application;

public class EstruturaSequencial {

	public static void main(String[] args) {
		
		// O computador começa executando aqui reservando um espaço na memória.
		double base, altura, area;
		
		base = 10;
		altura = 8;
		
		
		area = (base * altura) / 2;
		
		System.out.println(area);

	}

}
